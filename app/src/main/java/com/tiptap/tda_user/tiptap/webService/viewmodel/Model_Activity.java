package com.tiptap.tda_user.tiptap.webService.viewmodel;

public class Model_Activity {

    private String id_activity;
    private String number_activity;
    private String title_activity;
    private String note_activity;
    private String row_version;

    public Model_Activity() {}

    public String getId_activity() {
        return id_activity;
    }

    public void setId_activity(String id_activity) {
        this.id_activity = id_activity;
    }

    public String getNumber_activity() {
        return number_activity;
    }

    public void setNumber_activity(String number_activity) {
        this.number_activity = number_activity;
    }

    public String getTitle_activity() {
        return title_activity;
    }

    public void setTitle_activity(String title_activity) {
        this.title_activity = title_activity;
    }

    public String getNote_activity() {
        return note_activity;
    }

    public void setNote_activity(String note_activity) {
        this.note_activity = note_activity;
    }

    public String getRow_version() {
        return row_version;
    }

    public void setRow_version(String row_version) {
        this.row_version = row_version;
    }
}
